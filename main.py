strip = neopixel.create(DigitalPin.P0, 160, NeoPixelMode.RGB)
range2 = strip.range(0, 32)
range22 = strip.range(32, 32)
range3 = strip.range(64, 32)
range4 = strip.range(96, 32)
range5 = strip.range(128, 32)
range2.show_rainbow(1, 360)
range22.show_rainbow(1, 100)
range3.show_rainbow(1, 360)
range4.show_rainbow(1, 360)
range5.show_rainbow(1, 360)

def on_forever():
    range2.rotate(1)
    range22.rotate(-1)
    range3.rotate(1)
    range4.rotate(-1)
    range5.rotate(1)
    range2.show()
basic.forever(on_forever)
